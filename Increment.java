class Increment {
  public static void main(String[] args) {
    // ++, increment; --, decrement
    
    int x = 10, y = 10;
    
    System.out.println("x++ is " + x++);
    System.out.println("++y is " + ++y);
    System.out.println("x is " + x);
    System.out.println("y is " + y);
    
  }//main()
}